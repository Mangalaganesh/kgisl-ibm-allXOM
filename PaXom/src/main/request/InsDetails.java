package main.request;


public class InsDetails {

	private String insCode;
	private String insDob;
	private double insPlanContri;

	public String getInsCode() {
		return insCode;
	}

	public void setInsCode(String insCode) {
		this.insCode = insCode;
	}

	public String getInsDob() {
		return insDob;
	}

	public void setInsDob(String insDob) {
		this.insDob = insDob;
	}

	public double getInsPlanContri() {
		return insPlanContri;
	}

	public void setInsPlanContri(double insPlanContri) {
		this.insPlanContri = insPlanContri;
	}

}
