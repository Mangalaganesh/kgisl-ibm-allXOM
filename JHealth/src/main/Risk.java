	private String riskType;

	private String riskTypeDesc;

	public String getMainClsCode() {
		return mainClsCode;
	}

	public void setMainClsCode(String mainClsCode) {
		this.mainClsCode = mainClsCode;
	}

	public String getProductCode() {
		return productCode;
	}

	public void setProductCode(String productCode) {
		this.productCode = productCode;
	}

	public String getAgtGrpCode() {
		return agtGrpCode;
	}

	public void setAgtGrpCode(String agtGrpCode) {
		this.agtGrpCode = agtGrpCode;
	}

