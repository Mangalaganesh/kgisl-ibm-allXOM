
public class RiskCriteriaIntNONIGItemLevel {
	
	private String riskCode;
	private String consClsCode;
	private String occCode;
	
	
	
	
	public RiskCriteriaIntNONIGItemLevel() {
		super();
	}
	public RiskCriteriaIntNONIGItemLevel(String riskCode, String consClsCode, String occCode) {
		super();
		this.riskCode = riskCode;
		this.consClsCode = consClsCode;
		this.occCode = occCode;
	}
	public String getRiskCode() {
		return riskCode;
	}
	public void setRiskCode(String riskCode) {
		this.riskCode = riskCode;
	}
	public String getConsClsCode() {
		return consClsCode;
	}
	public void setConsClsCode(String consClsCode) {
		this.consClsCode = consClsCode;
	}
	public String getOccCode() {
		return occCode;
	}
	public void setOccCode(String occCode) {
		this.occCode = occCode;
	}
	

}
